package com.github.zhangkaitao.shiro.chapter16.entity;

import java.io.Serializable;

public class PatientFrom implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -3275851351595218502L;
	private Long id;
	private String name;
	private Long pid;
	private String ClassType;
	private String image;
	private String href;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getPid() {
		return pid;
	}
	public void setPid(Long pid) {
		this.pid = pid;
	}
	public String getClassType() {
		return ClassType;
	}
	public void setClassType(String classType) {
		ClassType = classType;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	@Override
	public String toString() {
		return "PatientFrom {id=" + id + ", name=" + name + ", pid=" + pid
				+ ", ClassType=" + ClassType + ", image=" + image + ", href="
				+ href + "}";
	}
	
	
	
}
